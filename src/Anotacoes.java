// Fundamentos de Programação Orientada a Objetos com Java

// Conceitos Básicos:
// Linguagens de baixo-nível:
// Mais próximas do hardware e bytecodes
// Linguagens de alto-nível: 
// Sintaxe e melhor compreensão do desenvolvedor
// Programação estruturada:
// Paradigma de programação com estruturas sequenciais ou procedimentos lineares
// Programação orientada a objetos:
// Dados -> Campos ou atributos
// Códigos -> Procedimentos ou métodos
// Representação do mundo real com objetos -> Abstração do mundo real
// Programação estruturada:
// Voltada à procedimentos e funções definidas pelo usuário
// Programação orientada a objetos:
// Voltada à classes e objetos

// Classes
// Todo aquivo Java é denominado classe -> Compilação de .java para .class
// Identificador, estados(atributos ou propriedades), comportamentos e instância
// Criação de objetos que representam uma classe e suas características

// Classe de modelo (model) -> estrutura de domínio -> Cliente, Pedido
// Classe de serviço (service) -> Regras e validação
// Classe de repositório (repository) -> Integração com banco de dados
// Classe de controle (controller) -> Comunicação externa à aplicação, http web ou webservices
// Classe utilitária (util) -> Recursos comuns à aplicações

// Pacotes
// Organização de classes -> Subdiretórios do src
// Convenções de pacotes
// Ver exemplo em "com.mastersoft.clinical"

// Visibilidade dos Recursos
// Modificadores (public, static, void)
// public -> qualquer classe em outro pacote pode visualizar esse recurso
// default -> classe oculta em outros pacotes
// private -> nem classes no mesmo pacote conseguem ver

// Getters e Setters
// Getter -> Retorna o valor do atributo especificado
// Setter -> Define outro novo valor para o atributo especificado
// Ver exemplo em "escola"

// Construtores
// Instanciamento de objetos
// Ver exemplo em "Pessoa.java"
// Uso da palavra reservada "this"

// Enums
// Enum -> Tipo especial de classe
// Objetos são previamente criados
// Exemplo em EstadoBrasileiro

// UML
// Linguagem de Modelagem Unificada
// Diagramas de Classes
// Diagramas de Objetos
// Associação -> Simples, bidirecional, agregação, composição
// Dependência
// Agregação -> Não está associada à existência, não tem dependência
// Composição -> São dependentes
// Multiplicidade
// 1. -> Associação de um elemento
// *. -> Associação de lista de elementos
// 0..1 -> Associação de zero ou um elementos
// 0..* -> Associação de zero ou uma lista de elementos
// Visibilidade
//(+) -> Público
//(#) -> Protegido
